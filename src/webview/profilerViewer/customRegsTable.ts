export const isColorReg = (offset: number): boolean => offset >= 0x180 && offset <= 0x1be;

// from 68k-dis.ts; make a u16 signed.
const coerce16 = (x: number) => (x ^ 0x8000) - 0x8000;

// Bitplane/sprite modulos are signed 16-bit line increments; everything else displays as hex.
export function formatRegValue(name: string, value: number): string {
  if (/MOD$/.test(name)) {
    const v = coerce16(value);
    return (v < 0 ? "-" : "") + Math.abs(v).toString(10);
  }
  return `$${(value & 0xffff).toString(16).padStart(4, "0")}`;
}
