export enum BLTCON0Flags {
  USEA = 1 << 11, USEB = 1 << 10, USEC = 1 << 9, USED = 1 << 8,
  LF7 = 1 << 7, LF6 = 1 << 6, LF5 = 1 << 5, LF4 = 1 << 4,
  LF3 = 1 << 3, LF2 = 1 << 2, LF1 = 1 << 1, LF0 = 1 << 0,
}

export enum BLTCON1Flags {
  DOFF = 1 << 7, EFE = 1 << 4, IFE = 1 << 3, FCI = 1 << 2, DESC = 1 << 1, LINE = 1 << 0,
  // line mode
  SIGN = 1 << 6, SUD = 1 << 4, SUL = 1 << 3, AUL = 1 << 2, SING = 1 << 1,
}

